package backend.dtos.references;

public class KeywordReference {

	private Long id;
	private String name;

	public KeywordReference() {
		// Default constructor
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}